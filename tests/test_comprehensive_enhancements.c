#include <stdio.h>
#include <stdlib.h>

// 综合测试：注释处理和汉字编码
int main() {
    // 测试1：单行注释中的&字符不应该影响检测
    int *指针1; // 注释中包含&字符
    *指针1 = 42; // 解引用野指针
    
    /* 测试2：多行注释
       包含各种字符，包括& */
    char *字符串1; /* 行内多行注释 */
    字符串1[0] = 'A'; // 解引用野指针
    
    /*
     * 测试3：跨行多行注释
     * 应该被完全忽略
     */
    double *数组1; // 未初始化的指针
    数组1[0] = 3.14; // 解引用野指针
    
    // 测试4：scanf检测（注释中包含&）
    char 字符串2[100];
    scanf("%s", 字符串2); // 正确：字符串不需要&
    
    int 整数1;
    scanf("%d", 整数1); // 错误：整数需要&
    
    // 测试5：结构体指针（汉字变量名）
    struct Point {
        int x, y;
    };
    
    struct Point *点指针1; // 未初始化的结构体指针
    点指针1->x = 10; // 解引用野指针
    点指针1->y = 20; // 解引用野指针
    
    // 测试6：指针赋值传播（汉字变量名）
    int *指针2; // 未初始化的指针
    int *指针3 = 指针2; // 将野指针赋值给另一个指针
    *指针3 = 200; // 解引用野指针
    
    return 0;
}
